# Словники (dict)

#1. створення словника - послідовність елементів парою (хеш-таблиця)

user = {
    'name': 'Bob',
    'age': 25,
    'login': 'admin',
    'password': 'jdfjhidfjgi'
}

#2. Робота з ключами
print(user['name']) # отримання значення ключа
print(user['age'])
print(user['login'])

user['age'] = 27 # заміна значення по ключу
print(user)

user['job'] = "python" # створення нової пари
print(user)
#3 Методи словника
value = user.pop('job') #видаляє пару по ключу
print(value) # повертає значення видаленої пари

item = user.popitem() # видаляє останню пару
print(item) #  видалену пару повертає кортежем(ключ, значення)

