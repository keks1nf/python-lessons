#4. Ітерація

for el in user: # при будь-якій звичайній ітерації словник повертає ключі
    print(f'--el')

for value in user.values(): print(f'--{value}') # ітерація значень

for key, value in user.items(): # ітерація пар (повертає кортеж (ключ, значення)
    print(f'{key}:{value}')


'''
Методи	Опис
pop()	Видаляє елемент із зазначеним ключем.
update()	Додає або змінює елементи словника.
clear()	Видаліть усі елементи зі словника.
keys()	Повертає всі ключі словника.
values()	Повертає всі значення словника.
get()	Повертає значення вказаного ключа.
popitem()	Повертає останній вставлений ключ і значення як кортеж.
copy()	Повертає копію словника.

Функція	Опис
all()	Повертає True, якщо всі ключі словника дорівнюють True (або якщо словник порожній).
any()	Повертає True, якщо хоч один із ключів словника дорівнює True. Якщо словник порожній, повертається False.
len()	Повертає довжину (кількість елементів) словника.
sorted()	Повертає новий відсортований список ключів у словнику.
clear()	Видаляє всі елементи зі словника.
keys()	Повертає новий об’єкт ключів словника.
values()	Повертає новий об’єкт значень словника.



| Метод                      | Опис                                                          | Приклад                                                   |
| -------------------------- | ------------------------------------------------------------- | --------------------------------------------------------- |
| **`dict.keys()`**          | Повертає всі ключі словника.                                  | `d = {"a":1,"b":2}` → `d.keys()` → `dict_keys(['a','b'])` |
| **`dict.values()`**        | Повертає всі значення словника.                               | `d.values()` → `dict_values([1,2])`                       |
| **`dict.items()`**         | Повертає список пар (ключ, значення).                         | `d.items()` → `dict_items([('a',1),('b',2)])`             |
| **`dict.get(k, def)`**     | Повертає значення за ключем `k`, або `def`, якщо ключа немає. | `d.get("c",0)` → `0`                                      |
| **`dict.update()`**        | Оновлює словник іншими значеннями.                            | `d.update({"b":5,"c":3})` → `{'a':1,'b':5,'c':3}`         |
| **`dict.pop(k, def)`**     | Видаляє ключ `k` і повертає його значення (або `def`).        | `d.pop("a")` → `1`                                        |
| **`dict.popitem()`**       | Видаляє і повертає останню пару (ключ, значення).             | `d.popitem()` → `('c',3)`                                 |
| **`dict.clear()`**         | Видаляє всі елементи зі словника.                             | `d.clear()` → `{}`                                        |
| **`dict.copy()`**          | Повертає копію словника.                                      | `d2 = d.copy()`                                           |
| **`dict.fromkeys(seq,v)`** | Створює словник з ключами із `seq`, значення однакове `v`.    | `dict.fromkeys(['x','y'],0)` → `{'x':0,'y':0}`            |
| **`dict.setdefault(k,v)`** | Повертає значення за ключем `k`. Якщо немає — додає `k:v`.    | `d.setdefault('z',10)` → додасть `'z':10`                 |
| **`dict.__contains__(k)`** | Перевірка, чи ключ у словнику (`in`).                         | `'a' in d` → `True`                                       |
| **`dict.__len__()`**       | Повертає кількість елементів.                                 | `len(d)`                                                  |

'''

#5. Сортування
humans = {
    'Alice': 20,
    'Bob': 27,
    'Charlie': 22,
    'David': 19,
    'Eve': 19,
    'Fred': 19,
}

print(dict(sorted(humans.items(), key=lambda item: item[1]))) # сортування за value
print(dict(sorted(humans.items(), key=lambda item: len(item[0])))) # сортування за key

